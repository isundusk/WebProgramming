
document.querySelector("#play1").onclick = function() {
    document.querySelector("#vid1").play();    
}
document.querySelector("#pause1").onclick = function() {
    document.querySelector("#vid1").pause();    
}
document.querySelector("#fast1").onclick = function() {
    document.querySelector("#vid1").playbackRate = 4.0;    
}
document.querySelector("#slow1").onclick = function() {
    document.querySelector("#vid1").playbackRate = 1.0;    
}
document.querySelector("#loud1").onclick = function() {
    document.querySelector("#vid1").volume = 80;    
}
document.querySelector("#soft1").onclick = function() {
    document.querySelector("#vid1").volume = 30;    
}



